// Define a list of animals with a common characteristic
const animals = ["dog", "cat", "rabbit"];

// Loop through the list and print the name of each animal
for (const animal of animals) {
  console.log(animal);
}

// Loop through the list and print a statement about each animal
for (const animal of animals) {
  console.log(`A ${animal} would make a great pet.`);
}

// Print a sentence about what these animals have in common
console.log("Any of these animals would make a great pet!");
