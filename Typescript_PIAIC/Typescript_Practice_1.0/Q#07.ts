// Numbers declaration
let Num1: number = 2;
let Num2: number = 4;
let Num5: number = 10;
let Num4: number = 16;

// Additiom
console.log(`Number ${Num2} when added with itself gives ${Num2 + Num2}`);

// Subtractiom
console.log(`Number ${Num5} when subtracted with ${Num1} gives ${Num5 - Num1}`);

// Multiplication
console.log(`Number ${Num2} when multiplied with ${Num1} gives ${Num2 * Num1}`);

// Division
console.log(`Number ${Num4} when divided with ${Num1} gives ${Num4 / Num1}`);